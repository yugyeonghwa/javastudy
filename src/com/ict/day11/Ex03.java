 package com.ict.day11;

public class Ex03 {
	private String name;
	private int age;
	private boolean live;

	public Ex03() {
		name = "큰뿔소";
		age = 3;
		live = false;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public boolean isLive() {
		return live;
	}

	public void setLive(boolean live) {
		this.live = live;
	}
}
